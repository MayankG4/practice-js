/**
 * Variables are useed to store pieces of information.
 */

 /**
  * Variables can be declared in three ways.
  * 
  * 1. using 'var'
  */
 var x = 42; // this syntax can be used to declare both local and global variables

 /**
  * 2. by simply assigning it to a value
  */
 y = 42; // if this form is used outside of a function, it declares a global variable. It generates a strict JavaScript warning.
         // this form should be avoided.

/**
 * 3. by using the 'let' keyword
 */
let y = 13 // this syntax can be used to declare a block-scope local variable
